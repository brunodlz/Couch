import UIKit

extension UIImage {
    static func user()  -> UIImage { return UIImage(named:"user")!  }
    static func couch() -> UIImage { return UIImage(named:"couch")! }
    static func next()  -> UIImage { return UIImage(named: "next")! }
    static func previous()  -> UIImage { return UIImage(named: "previous")! }
}
